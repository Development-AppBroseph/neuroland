import 'package:grow_food/features/presentation/auth/domain/entiti/user_sign_up_entiti.dart';

class UserSignUpModel extends UserSignUpEntiti {
  UserSignUpModel({
    required final bool success,
    required final List<dynamic> errors,
    required final SuccessEntiti model,
  }) : super(
          success: success,
          errors: errors,
          model: model,
        );
  factory UserSignUpModel.fromJson(Map<String, dynamic> json) =>
      UserSignUpModel(
          success: json['success'],
          errors: json['errors'],
          model: SuccessModel.fromJson(json['model']));
  Map<String, dynamic> toJson() => {
        'success': success,
        'errors': errors,
        'model': model,
      };
}

